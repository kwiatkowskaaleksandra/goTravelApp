package biuropodrozy.gotravel.security.oauth2;

/**
 * The OAuth2Provider enum defines the possible providers for OAuth2 authentication.
 */
public enum OAuth2Provider {
    /**
     * Represents the local authentication provider.
     */
    LOCAL,

    /**
     * Represents the Google authentication provider.
     */
    GOOGLE,

    /**
     * Represents the Facebook authentication provider.
     */
    FACEBOOK
}
